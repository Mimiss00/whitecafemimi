    public void setTime() {
        new Thread(new Runnable() {
            @Override
            public void run() {
                while (true) {
                    try {
                        Thread.sleep(1000);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(MainMenu_cust.class.getName()).log(Level.SEVERE, null, ex);
                    }

                    SwingUtilities.invokeLater(new Runnable() {
                        @Override
                        public void run() {
                            Date date = new Date();
                            SimpleDateFormat tf = new SimpleDateFormat("h:mm:ss aa");
                            SimpleDateFormat df = new SimpleDateFormat("EEEE, dd-MM-yyyy");
                            String time = tf.format(date);
                            txTime.setText(time.split(" ")[0] + " " + time.split(" ")[1]);
                            txDate.setText(df.format(date));
                        }
                    });
                }
            }
        }).start();
    }


private void writeUserData() {
    String[] foodItems = {
            jLabel8.getText(),
            jLabel9.getText(),
            jLabel12.getText(),
            jLabel17.getText(),
            jLabel22.getText(),
            jLabel45.getText(),
            jLabel41.getText(),
            jLabel73.getText(),
            jLabel68.getText(),
            jLabel50.getText(),
    };

    String payment = jTextField3.getText();

    try (BufferedWriter writer = new BufferedWriter(new FileWriter("cust_food.txt", true))) {
        // Write selected food items
        writer.write(purchasedID + ";");

        StringBuilder itemsLine = new StringBuilder();

        for (int i = 0; i < foodItems.length; i++) {
            if (isSelected(i)) {
                // Append to the itemsLine
                itemsLine.append(foodItems[i]).append(";");
            }
        }

        // Remove the trailing semicolon if there are selected items
        if (itemsLine.length() > 0) {
            itemsLine.deleteCharAt(itemsLine.length() - 1);
        }

        // Write the items line
        writer.write(itemsLine.toString() + ";" + payment + "\n");

        JOptionPane.showMessageDialog(null, "Successfully saved");
    } catch (IOException e) {
        JOptionPane.showMessageDialog(null, "Error saving order details to file");
    }
}

    

      private boolean isSelected(int foodIndex) {
    
    switch (foodIndex) {
        case 0:
            return jCheckBoxsteak.isSelected();
        case 1:
            return jCheckBoxpizza.isSelected();
        case 2:
            return jCheckBoxpasta.isSelected();
        case 3:
            return jCheckBoxburger.isSelected();
        case 4:
            return jCheckBoxtart.isSelected();
        case 5:
            return jCheckBoxpudding.isSelected();
        case 6:
            return jCheckBoxtiramisu.isSelected();
        case 7:
            return jCheckBoxcoffee.isSelected();
        case 8:
            return jCheckBoxtea.isSelected();
        case 9:
            return jCheckBoxmilkshake.isSelected();
        default:
            return false;
    }
}



